int numero1 = 5;
int numero2 = 8;

// Operadores relacionais
// Servem para realizarmos comparativos

// Maior
// Usa > igual no Python
// Quando fazemos um comparativo o retorno será lógico (True ou False)
Console.WriteLine(numero1 > numero2);
// Se for verdadeiro retorna True e se for falso retorna 


// Maior ou igual
// Usa >= igual no Python
Console.WriteLine(numero1 >= numero2);

// Menor
// Usa < igual no Python
Console.WriteLine(numero1 < numero2);

// Menor ou igual
// Usa <= igual no Python
Console.WriteLine(numero1 <= numero2);

//igual
// Usa == igual no Python
Console.WriteLine(numero1 == numero2);


// diferente
// Usa != igual ao Python
Console.WriteLine(numero1 != numero2);
