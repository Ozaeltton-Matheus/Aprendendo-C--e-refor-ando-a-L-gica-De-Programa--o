// Variaável de indice
int indice = 20;

// Laço de repetição faça enquanto (do while)

do
{
    Console.WriteLine(indice);
    indice++;
}
while(indice <=10); 

// A diferença do while (enquanto) para o do while (faça enquanto) é que o while vai primeiro verificar se vai fazer a ação e caso sim ele executa já o do while primeiro executa alguma ação e depois ele faz uma validação (verificação) se deve continuar ou não executando a ação

// Nesse caso o do while vai Imprimir o valor do indice que nesse caso é 20 e depois ele vai verificar se o indice é menor == a 10, e como não é ele vai exibir sommente o valor do indice que é 20 uma vez e vai parar a execução do laço de repetição.

// Dependendo da estrutura do projeto que você fpr desenvolver você escolhe o while ou o do while.

