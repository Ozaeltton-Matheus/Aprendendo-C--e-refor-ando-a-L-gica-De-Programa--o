// Criacão de variáveis
int numero1 = 30;
int numero2 = 3;

// A divisão em C# é como no Python com uma /
int resultado_divisao = numero1 / numero2;
// Operadpres aritméticos
// Divisão
Console.WriteLine($"{numero1} dividido por {numero2} é igual a {resultado_divisao}");

// Multiplicação
// A multiplicação em C# é como em Python com o *
int resultado_multiplicacao = numero1 * numero2;
Console.WriteLine($"A multiplicação de {numero1} com {numero2} é igual a {resultado_multiplicacao}");

// Subtração
// A subtração é como no Python com o -
int resultado_subtracao = numero1 - numero2;
Console.WriteLine($"{numero1} subtraido por {numero1} é igual a {resultado_subtracao}");

// Adição
// A adição é commo no Python com o +
int resultado_adicao = numero1 + numero2;
Console.WriteLine($"{numero1} adicionado por {numero2} é igual a {resultado_adicao}");

// Resto da divisão (Módulo)
// O resto da divisão é igual no Python com %
int resultado_resto_da_divisao = numero1 % numero2;
Console.WriteLine($"O resto da divisão de {numero1} dividido com {numero2} é igual a {resultado_resto_da_divisao}");

// Incrementador
// Semelhante ao variavél = variável + 1 ou variável += 1
numero1++;
Console.WriteLine($"{numero1}");

// Decrementador
numero2--;
Console.WriteLine($"{numero2}");


