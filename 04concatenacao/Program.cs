// Variáveis
string nome = "Matheus";
string cidade = "Cerro Corá";

// Concatenação através do +
// Concatena variáveis com strings (textos)

Console.WriteLine(nome + " mora em " + cidade + ".");

// Concatenação através de interpolação de Strings (textos)
// Semelhante a uma das opções de concatenação de Strings (textos) presentes no Python, que no caso é o print(f{nome} mora em {cidade}.")
Console.WriteLine($"{nome} mora em {cidade}.");
