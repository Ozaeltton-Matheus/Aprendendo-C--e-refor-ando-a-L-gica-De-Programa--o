// Estrutura de repetição enquanto (while)

// Váriavel de indice
int indice = 1;

//laço de repetição enquanto
while(indice <= 10)
{
    Console.WriteLine(indice);
    indice++; // É necessário incrementar com o indice++ para que o laço de repetição não fique em um cilo infinito exibindo somente 1
}